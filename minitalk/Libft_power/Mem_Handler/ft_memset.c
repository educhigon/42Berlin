#include <unistd.h>

void	*ft_memset(void *s, int c, unsigned long n)
{
	unsigned long	i;
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
