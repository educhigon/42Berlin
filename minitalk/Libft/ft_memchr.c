void	*ft_memchr(const void *s, int c, unsigned long n)
{
	unsigned int	i;
	unsigned char	*src;

	src = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (src[i] == (unsigned char)c)
			return (src + i);
		i++;
	}
	return (0);
}
