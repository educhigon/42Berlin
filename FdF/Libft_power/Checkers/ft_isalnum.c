int	ft_isalnum(int c)
{
	char	uc;

	if (c < 0 || c > 255)
		return (0);
	uc = (unsigned char)c;
	if ((uc >= 'A' && uc <= 'Z')
		|| (uc >= 'a' && uc <= 'z')
		|| (uc >= '0' && uc <= '9'))
		return (1);
	return (0);
}
